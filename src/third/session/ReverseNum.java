package third.session;

public class ReverseNum {
	public static void main(String[] args) {
		for(int i=10;i>=1;i--)
		{
			System.out.print(i+" ");//used sysout of print to take screenshot of output
		}
		System.out.println();
		int a=10;
		while(a>=1)
		{
			System.out.print(a+" ");
			a--;
		}
	}

}
